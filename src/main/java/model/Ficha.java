package model;

public class Ficha {
    int codFicha, cantiApre, codProgra, codApren;

    public Ficha() {
    }

    public int getCodFicha() {
        return codFicha;
    }

    public void setCodFicha(int codFicha) {
        this.codFicha = codFicha;
    }

    public int getCantiApre() {
        return cantiApre;
    }

    public void setCantiApre(int cantiApre) {
        this.cantiApre = cantiApre;
    }

    public int getCodProgra() {
        return codProgra;
    }

    public void setCodProgra(int codProgra) {
        this.codProgra = codProgra;
    }

    public int getCodApren() {
        return codApren;
    }

    public void setCodApren(int codApren) {
        this.codApren = codApren;
    }
 
    
}
